                <h4 class="h4boks">Tilføj klip</h4>
            
                <p class="tekst-i-boks">Det er nemt og simpelt at tilføje klip til Premiere Rush. Man kan bare trække sine filer ind på linjen. Bemærk den smarte ”magnet”-funktion, der gør at man ikke efterlader afstand imellem de forskellige klip. Rush rykker dem automatisk sammen. (Softworld - DK, 2019) Hvis man ønsker at tilføje lydklip, så fungerer det på helt samme måde.</p>
               
