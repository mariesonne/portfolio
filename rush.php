                <h3 class="h3" id="rush">Hvad er Premiere Rush?</h3>    

                <p class="tekst-i-boks">Premiere Rush er et videoredigerings program som er skabt af Adobe. Premiere Rush skal ses som en lettere udgave af deres populære program Premiere Pro. (Softworld - DK, 2019) Premiere Rush har alle de bedste og vigtigste funktioner fra Premiere Pro, dog er de mest avancerede funktioner skåret fra. (ibid.) Det gør programmet let at bruge. Man kan lynhurtigt lærer at lave små lækre videoer. Derudover kan Premiere Rush også hentes til telefonen, så man kan redigere videoen direkte efter at have filmet det.  </p>

            </section>
            
            <h3 class="h3" id="video">Videoproduktion</h3>
            
            <p class="tekst">Vi bruger først Premiere Rush i slutningen af processen når producere videoer. Vi kan dele videoproduktion op i tre processer. (bilag quick) Pre-production: altså planlægning af optagelser, production: det er her optagelserne finder sted og post-production: redigering af optagelserne.</p>
            
            <br><br>
            
            <h4>Pre-production</h4>
            
            <p class="tekst">Vi skal her have klargjort vores mål og formål. Vi skal overveje hvem vores målgruppe er. Er det en målgruppe der kan klare noget humor eller skal de være en mere seriøs video. Vi skal finde ud af hvordan vores produkt skal præsenteres. (ibid.)</p>
            
            <p class="tekst">Vi skal også overveje tekniske dele. Det kan f.eks. være om der skal være musik, og evt. hvilken genre musik. Det kan også være om der skal være en fortæller. </p>
            
            <p class="tekst">Når vi har styr på hvad der skal ske i videoen, er det vigtigt at vi bruger god tid på at lave en ”shot-list”, der indeholder alle optagelser. </p>
            
            
                <img id="story" src="billeder/storyboard.png">
                
            
            
            <p class="tekst">Her ses et storyboard eller ”shot-list” som vi lavede til den video vi skulle lave i undervisningen. Vi brugte det under optagelser. Den er meget vigtig, så man undgår at mangle nogle optagelser. </p>
            
            <br><br>
            
            <h4>Production</h4>
            
            <p class="tekst">Det er her vi skal filme vores klip til videoen. Til opgaven vi lavede på skolen, skulle vi bruge smartphone og naturligt lys. Det kunne også sagtens have været professionelt udstyr, men i den virkelige verden, har man ofte ikke mere til rådighed end det. </p>
            
            <p class="tekst">Det er en god ide at optage 4-5 sekunder i starten og i slutningen. (ibid.) Dette er vigtigt så man har lidt at gøre med under redigeringen. Når man optager, er det vigtigt at man filmer, så det passer til mediet. Så hvis det skal fremvises på bredformat, så skal man ikke filme på højformat. </p>
            
            <br><br>
            
            <h4>Post-production</h4>
            
            <p class="tekst">Det er her vi redigerer vores optagelser. Vi skulle til opgaven på skolen bruge Premiere Rush – Men til større opgaver vil man ofte bruge Premiere Pro i stedet. Det er også her vi skal finde musik og andre lyde til vores video. Dette er noget man nemt kan komme til at bruge for lang tid på, så det er her vigtigt at begrænse sig. (ibid.) Det er vigtigt at huske på, at der er copyright på meget musik og noget af det bliver fjernet hvis når det uploades til YouTube. </p>
            
            
