            <h3 class="h3" id="re">Research </h3>
            
            <p class="tekst">Vi brugte desk-reseach til at finde alt hvad vi kunne om honningsnitter, Karen Volf, Bisca og kager generelt. Vi gjorde os nysgerrige på emnet, ved at notere spørgsmål vi ønskede besvaret på et whiteboard. Til sidst organiserede vi alle vores informationer på en tavle med sticky notes. Vi fandt ud af, at det var mere overskueligt at inddele vores information i små overemner. </p>
            
            <h3 class="h3" id="ide">Ideudvikling og wireframing</h3>
            
            <p class="tekst">Vi fandt ret hurtigt en ide. Jeg tror desværre vi fik låst os for meget fast på den ene ide, og det gjorde at vores ideudvikling ikke varede så lang tid. Når man låser sig fast på en ide, så ”stopper” man på en måde den kreative proces og vi gik muligvis glip af nogle gode ideer.  </p>
            
            <img src="billeder/brain.png" width="90%">
            
            <p class="tekst"> Vi fik lavet wireframes af vores hjemmeside. Vi lavede både til mobil, tablet og pc. Derudover lavede vi også et moodboad, så man kunne fornemme, hvilken stil vi ønskede på hjemmesiden</p>
           
            <p class="tekst"> Vi testede vores wireframes på vores peer-gruppe. Peer-grupperne fungerede rigtig godt, og vi fik meget konstruktiv kritik, som vi kunne arbejde videre med. Udover vores peer-grupper, så arbejde vi generelt rigtig god sammen i klassen. Vi oplevede hvordan vi kunne hjælpe hinanden på kryds og tværs til alle de små spørgsmål der dukker op. </p>
            
            <h3 class="h3" id="xd">High fidelity prototyping</h3>
            
            <p class="tekst">Vi skulle lave en high-fidelity prototype med Adobe XD. Jeg havde allerede brugt programmet i et tidligere projekt, så vi havde ingen problemer med at lave dem. Dog brugte vi Lorem Ipsum-vrøvletekst. Dette vil jeg undgå en anden gang, da man designer siden, uden at vide hvor meget den rigtige tekst egentligt fylder. </p>
            
