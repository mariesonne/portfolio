                <h3 class="h3" id="projekt">Projektet</h3> 
                
                <p class="tekst-i-boks">Vi fik til dette projekt udleveret et fysisk produkt, som der skulle udarbejdes et one-page website til. Formålet kunne være salg, instruktion, promotion, oplysning osv. men vi måtte helt selv bestemme. </p>
                
