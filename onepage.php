            <h3 class="h3" id="implementering">Implementering</h3>
            
            <p class="tekst">Da designet var fastlagt, skulle vi i gang med at producere siden. Vi fik først og fremmest styr på hvad der skulle produceres og hvornår vi skulle sætte tid af til det. Vi skulle selvfølgelig kode vores website, derudover skulle vi have filmet end video og have taget nogle billeder der skulle indgå på siden.  </p>
            
            <p class="tekst"> Da det var et One-page-site gjorde det det meget svært at sidde 3 personer og kode, da det kun er én HTML-fil. Dette kunne have været løst med GitHub. Vi sad ofte alle 3 og kiggede på én skærm. Det tager delfølgelig lang tid, når vi ikke delte det ud, men på den måde, lærer vi alle 3 at kode.</p>
            
            <p class="tekst"> Da jeg havde udstyr osv. hjemme hos mig, endte det med, at jeg tog billederne. One Page-projektet var et projekt hvor vi alle skulle lære. Så det burde have været den med mindst erfaring med foto, der skulle have haft taget dem, så han eller hun kunne have fået mere erfaring indenfor emnet. Dette vil jeg helt bestemt tage med fremover, så alle i gruppen kan lærer nye ting – det er nemlig vigtigere end et godt resultat. </p>
            
