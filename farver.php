                <h3 class="h3" id="farverum">Farverum</h3>    
            
                <p class="tekst-i-boks">Der er to forskellige farverum: RGB og CMYK. RGB er bedst når noget skal vises digitalt og CMYK er bedst til printede produkter. (Ellis, 2019) </p>
               
               
                <h4 class="h4boks">RGB</h4> 
               
                <p class="tekst-i-boks">RGB står for rød, grøn og blå. Når vi ser forskellige farver på vores skærm, er det faktisk en blanding af rød, grøn og blå og så en variation af deres intensitet. Alle farver starter altså som sort, fordi alle lys er slukket. Hvis rød, grøn og blå er tændt med samme styrke, skaber de ren hvid. (ibid.) </p>
               
               
               <h4 class="h4boks">CMYK</h4>
            
                <p class="tekst-i-boks">CMYK står for cyan, magenta, gul og sort. En printer skaber alle de farver vi ønsker ved at kombinere blæk i disse farver i varierende grad. (ibid.) Modsat RGB, så starter alle farver her som helt hvide. Vi trækker altså lys ud, hvor vi ved RGB tilføjede lys. Hvis alle farver blandes sammen, får vi sort. Det bliver dog ikke helt sort, så det er derfor vi har key eller sort med i CMYK. </p>
        
