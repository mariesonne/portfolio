        <h4>Synkron og asynkron kommunikation</h4>
        
        <p class="tekst">Når to har en samtale enten ansigt til ansigt eller en chat online er kommunikationen synkron. (ibid.) Ved asynkron kommunikation går der længere tid imellem svarene. Det kan eksempel være fysiske breve der bliver sendt. Det kan også være likes, kommentarer, mail osv. Der er altså en tidsforskydning i kommunikationen. (ibid.)</p>
        
