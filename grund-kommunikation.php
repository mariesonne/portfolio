            <h3 class="h3" id="det-samf-para">Det samfundsvidenskabelige paradigme</h3>    

            <p class="tekst-i-boks">Ved det samfundsvidenskabelige paradigme lægger fokus ikke på afsenderen, men udelukkende på afsenderen. (Busch, Grundlæggende kommunikationsteori, 2015) Afsenderen styrer altså her kommunikationssituationen. Ved det samfundsvidenskabelige paradigme tror man i høj grad på at verden er forudsigelig. Her tror man også på, at den der undersøger, kan sætte sig selv i en objektiv position. </p>

            <p class="tekst-i-boks">Teorierne og metoderne vi andevender trækkes fra de samfundsvidenskabelige fagområder f.eks. politik og jura. Man anvender meget kvantitative modeller og analyser. (bid.) Det vil altså sige, at man ofte ønsker et målbart resultat f.eks. hvor mange… Det samfundsvidenskabelige paradigme anvender meget gamle metoder og modeller, som blev lavet længe før internettet. Så man har altså en lidt ældre tilgang, hvor det humanistiske paradigme forsøger at imødekomme internettet. (ibid.) </p>
            
            <br><br>
            
            <h4>Kanyleteorien</h4>
            
            <p class="tekst-i-boks">Kanyleteorien er formuleret i 1927 af Harold D. Lasswell. Teorien påpeger hvor stor en effekt medierne har på os som mennesker. (ibid.) Hvis vi ser mange grove voldsfilm, så vil de påvirke vores måde at være på. Vi vil dermed få en mere voldelig tankegang og volden i vores samfund vil stige. (ibid.) Man kan derfor styre et helt samfund ved hjælp af medier. Det er også derfor teorien har sit navn, fordi man metaforisk set kan sprøjte ideer, tanker og holdninger ind i modtageren. </p>
            
            <br><br>
            
            <h4>S-R-Modellen og S-O-R-modellen</h4>
            
            <p class="tekst-i-boks">S-R-modellen bygger på samme antagelse om, at mennesket kan styres igennem medier. (ibid.) S står for stimuli og R står for respons. Respons henviser til efter man har afsendt sit budskab. Jo større budskabet er jo kraftigere respons, vil man få. </p>
            
            <p class="tekst-i-boks">Ved S-O-R-modellen er der tilføjet organisme. Man mener ikke at man kan styre mennesket direkte, som man mener ved S-R-modellen. (ibid.) Mennesker er ikke ens, og dermed vil responsen heller ikke være ens. Man skal altså overveje hvilken målgruppe man har og hvilke hensyn der skal tages til lige netop denne målgruppe. (ibid.)</p>
            
            <br><br>
            
            <h4>Lasswells formel</h4>
            
            <p class="tekst-i-boks">Lasswell er kendt for sin berømte replik: </p>
            
            <p id="citat" class="tekst-i-boks">”En bekvem måde at beskrive en kommunikationshandling på er at besvare følgende spørgsmål: <br>
            Hvem <br>
            Siger hvad <br>
            Gennem hvilken kanal <br>
            Til hvem <br>
            Med hvilken effekt.”
            </p>
            
            <p class="tekst-i-boks">Her medtænker man mediet og dermed kommer der en betydning for hvilket medie man anvender. (ibid.) Selvom der her er opmærksomhed på modtageren, så er det stadig afsenderen der er i fokus. </p>
               
