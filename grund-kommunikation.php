            <h3 class="h3" id="grund-kom-teori">Grundlæggende kommunikationsteori</h3>

            <p class="tekst-i-boks">Man kan dele kommunikationsteorier op i to forskellige paradigmer – det humanistiske paradigme og det samfundsvidenskabelige paradigme. (Busch, Grundlæggende kommunikationsteori, 2015) Et paradigme er en måde at se verden på, og altså også en måde at se kommunikation på. De to paradigmer vi arbejder med, hænger sammen med de to forskellige videnskabelige områder, der har hver deres sæt af teorier og metoder. (ibid.) </p>
        
        </section>
        
         <h3 class="h3" id="det-human-para">Det humanistiske paradigme</h3>    

        <p class="tekst">Ved det humanistiske paradigme er der stor fokus på modtageren. (ibid.) Modtageren er her aktiv og medskabende. Det vil også sige, at modtageren er med til at bestemme hvordan budskabet skal forstås. Ved det humanistiske paradigme kommer teorierne og metoderne hovedsageligt fra de humanistiske fagområder, f.eks. sprog, kultur, psykologi osv. (ibid.) Under det humanistiske paradigme anvender man undersøgelsesmetoder, som giver en større forståelse for hvad mordtageren oplever. Man har en meget kvalitativ tilgang, fordi man ønsker at få en dybere forståelse. (ibid.)</p>

        <br><br>

        <h4>IMK-modellen</h4>
        
        <p class="tekst">En model der bliver brugt under det humanistiske paradigme, er IMK-modellen. (ibid.) IMK står for International Markeds Kommunikation. Ved IMK-modellen er teksten i fokus. Med tekst skal man ikke kun have fokus på almindelige tekster der indeholder ord, men billeder, grafik osv. hører også under tekst. Kontekst/kultur, medier og genre er med til forme teksten. (ibid.) Både afsender og modtager er nødvendige for, at der eksisterer en kommunikationssituation. Det er ikke nok, at afsenderen sender et budskab. Modtageren skal også deltage. (ibid.)</p>

        <br>

        <p class="tekst">Modellen er opbygget med teksten i centrum. Derudover er der afsenderen, modtageren, referenten, koden, kontekst/kultur, medier og genre.</p>

        <img id="imk" src="billeder/IMK-modellen.png" alt="IMK-modellen">
        
        <br><br>

        <h5>Tekst</h5>

        <p class="tekst">Teksten kan som sagt være flere forskellige ting. Men teksten kan forstås som den kommunikative instans, som afsenderen og modtageren skaber i fællesskab. (ibid.)</p>

        <br><br>

        <h5>Afsender</h5>

         <p class="tekst">Afsenderen er den der beslutter sig for hvordan og hvornår kommunikationen skal finde sted. I IMK-modellen skelner man mellem en tekstintern og en tekstekstern afsender. Ved en reklame for eksempel, så er reklamebureauet der har sat reklamen op den teksteksterne afsender. (ibid.) Den tekstinterne afsender er den der er tydeligt markeret i teksten. Det er f.eks. den virksomhed, reklamen bliver lavet for. Det kan også være nødvendigt at skelne mellem den primære og sekundære afsender. Den primære afsender er virksomheden i en annonce, hvor den sekundære f.eks. kunne være en specialist der fremhæver det produkt annoncen omhandler. (ibid.)</p>

          <br><br>

         <h5>Modtager</h5>

         <p class="tekst">Ved modtageren kan man også skelne imellem en tekstintern modtager og en tekstekstern modtager. (ibid.)  Den tekstinterne modtager er markeret i teksten. Den teksteksterne modtager er den der læser teksten. Denne modtager er ikke nødvendigvis indenfor målgruppen. Man kan som ved afsendere også opdele modtagere en primær og sekundær. Et eksempel kan være en lejetøjsreklame. Reklamen henvender sig til børn som er den primære modtager, hvor forældrene er den sekundære modtager, da det er dem der skal købe det reklamerede lejetøj. (ibid.)</p>

        <br><br>

         <h5>Referenten</h5>

        <p class="tekst">Referenten er det som teksten refererer til. (ibid.) Hvis der i teksten bliver reklameret for et produkt, så bliver der ofte refereret til hvilken effekt produktet giver. Det kan også være brandet bag produktet der bliver refereret til. Et eksempel er Heinz ketchup. Heinz er et mærke der er kendt i de fleste lande. Men indholdet er i høj grad det samme som en discount ketchup. Så en Heinz ketchup er ikke kun produktet, men også alt den værdi brandet giver og referere til hos modtageren. (ibid.)</p>

         <br><br>

         <h5>Koden</h5>

        <p class="tekst">Koden er det sprog som modtageren skal kunne, for at forstå afsenderen. (ibid.) Det er vigtigt at man her husker forskellige kulturer og dermed forskellige koder. Ord kan betyde forskellige ting i forskellige lande og kulturer, og derfor kan budskabet blive helt anderledes i andre kulture end i ens egen.</p>

        <br><br>

         <h5>Kontekst / Kultur</h5>
        
        <p class="tekst">Kontekst/kultur er den situation der indrammer kommunikationssituationen. (ibid.) Det vil altså sige at det er svaret på hvem, hvad, hvor og hvornår. Derudover er det er også den kultur, som situationen er påvirket af. Det er både internationalt og nationalt. Dette er meget vigtigt at have fokus på, da nogle kulturer er meget forskellige fra ens egen. Så noget som bliver godt modtaget i ens egen kultur, kan måske blive taget i mod på en helt anderledes måde i en anden kultur.</p>

        <br><br>
        
        <h5>Medie</h5>

        <p class="tekst">Mediet er det der sørger for, at teksten kommer frem til modtageren, altså en form for kommunikationskanal. Det kan både være printede, elektroniske eller mennesket selv. (ibid.) Mediet vil altid påvirke kommunikationssituationen. Der er stor forskel på om man gør brug af tv-reklamer eller en trykt avis. Det er hovedsagligt ældre mennesker der læser trykte aviser, derfor skal man ikke bruge dette medie, hvis man ønsker at ramme den yngre målgruppe. (Slots‐ og Kulturstyrelsen, 2017) Derudover skal man også overveje hvilket behov der skal tilfredsstilles hos modtageren, har modtageren nogle bestemte holdninger til de forskellige medier, hvad er brugssituationen af mediet, hvor ofte bruges mediet og hvordan kan alt dette påvirker kommunikationssituationen. (Busch, Grundlæggende kommunikationsteori, 2015)</p>


        <br><br>

        <h5>Genre</h5>

        <p class="tekst">Genre udpeger hvordan kultur/kontekst, medier og retoriske strategier kan arbejde sammen. (ibid.) Man kigger altså her trinvist på, det kommunikative formål, træstrukturer og de retoriske strategier.</p>
       
