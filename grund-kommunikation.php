            <h3 class="h3" id="kom-for-mul">Kommunikation for multimediedesignere</h3>    
        
            <p class="tekst-i-boks">Som multimediedesigner er det væsentligt at kunne formidle og kommunikere. 

            <br><br><span id="citat">Multimedier er medier, der samtidigt gør brug af flere forskellige udtrykssystemer – såsom tekst, levende billeder, stillbilleder, animation, grafik og lyd (tale, musik, lydeffekter) – og hvor disse udtryksformer er integreret i og styret af en digital computer eller et digitalt miljø”.</span> (Bilag ?1) <br><br>

            Det vil altså sige en multimediedesigner bruger kommunikation til en stor del af deres arbejde. Det kan for eksempel være når helt almindelig tekst skal produceres. Derudover bruges kommunikation også igennem design. Det kan f.eks. være et design på en hjemmeside. Farver, billeder osv. kommunikere mere end vi lige tror. Derudover bruger multimediedesigneren også kommunikation når han eller hun skal pitche ideer og forslag. 
            </p>
            
        </section>
        
        <h3 class="h3" id="hvad-er-kom">Hvad er kommunikation?</h3>    
        
        <p class="tekst">Kommunikation er ikke kun ord. (Andersen &amp; Jørnø, 2013) Ord er kun én slags kommunikation. Vi kommunikerer også med de valg vi tager, det tøj vi bærer, den måde vi dufter og endda også bare med vores tilstedeværelse. Vi kan endda også kommunikere noget, hvis vi ikke vælger at møde op. Der kommunikerer vi at vi måske er sure eller har brug for at være alene. Vi kommunikerer altså næsten altid, også selvom vi ikke er til stede. (ibid.) Det vil altså sige at vi ikke kan ikke-kommunikere. Ved kommunikation er der som regel en afsender, en modtager og et budskab, altså det der bliver meddelt. (ibid.) Selvom vi ikke ønsker at sende et budskab, så gør vi det stadig. Det kan f.eks. være to mennesker der står og venter på, at der bliver grønt ved fodgængerfelt. De taler ikke sammen, men de registrer og tolker hinanden. Kommunikation foregår konstant. (ibid.)</p>
        
        <br><br>
        
        <h4>Verbal og nonverbal kommunikation</h4>
            
        <p class="tekst">Som sagt er kommunikation meget mere end bare det vi siger. Verbal kommunikation dækker over det vi siger. (bilag ?1) Nonverbal kommunikation er derimod det vi ikke siger. Det dækker altså over mimik, kropssprog, toneleje osv. (ibid.) Den nonverbale sender altså meddelelser om hvor det verbale skal forstås. (ibid.)</p>
        
        <br><br>
        
        <h4>kongruent og inkongruent</h4>
        
        <p class="tekst">Når det verbale og det nonverbale spiller sammen kaldes det kongruent kommunikation. Det kunne f.eks. være hvis man smiler og siger ”hvor er du bare sød”. De man siger og det man udtrykker, hænger sammen, og modtageren vil derfor ikke være i tvivl om, at afsenderen virkelig mener det. (ibid.) Hvis de derimod ikke spiller sammen, kaldes det inkongruent kommunikation. Det kan f.eks. være hvis man siger ”hvor er du bare sød”, men ikke smiler. Afsenderen ser sur ud og vrisser. Modtageren vil her blive usikker på, om det der siges, også er det der menes. (ibid.)</p>
        
