            <h3 class="h3" id="fase1">Empathize</h3>
            
            <p class="tekst">I denne fase skal vi være nysgerrige. (ibid.) Og når vi ikke længere er nysgerrige, så skal vi stille spørgsmålstegn ved nye ting, så vi igen bliver nysgerrige. Vi skal samle information fra mange forskellige kilder og helst også fra flere forskellige synsvinkler hvis dette er muligt. Vi kan både bruge desk-research og field-research.  Vi skal forstå den kontekst som brugerne er i. Når vi har fundet alt hvad der er at finde om problemstillingen, skal vi organisere vores viden. Dette kan gøres visuelt f.eks. på et whiteboard. Dette er en divergent fase. </p>
            
            
            <p class="tekst" id="designtekst">Vi har bruger Design Thinking teorierne mange af de projekter vi har. Men vi især brugt det i Hackathon projektet og vores Design Thinking projekt. I begge projekter oplevede jeg hvor svært det er at lægge ideer på hylden. Jeg fandt dog ud af at det hjalp at skrive ideen på en lille lap og gemme den væk. På denne måde tænkte jeg ikke helt så meget på de ideerne der dukkede op i ens hoved. </p>
            
            <p class="tekst" id="designtekst">En anden udfordring jeg føler vi havde var at blive ved med at være nysgerrig. Dette var mest ved Design Thinking projektet om de 17 verdensmål. Jeg følte mig hurtigt mættet af informationer. </p>
           
            <p class="tekst" id="designtekst">Vi havde valgt målet om bæredygtige byer og lokalsamfund. Jeg havde allerede i forvejen min holdning til urbanisering og jeg kom derfor til at undgå kilder der var for urbanisering. Ved at stille nye spørgsmål til os selv, blev vi igen nysgerrige og fik fundet en masse nye informationer. Her er nogle af mine tanker omkring vores empathize forløb:</p>
            
