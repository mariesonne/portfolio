                <h3 class="h3" id="teori">Design Thinking teori</h3> 
                
                <p class="tekst-i-boks">Design Thinking er ikke kun en proces, det er også en måde at tænke på. Med disse metoder og teknikker kan vi løse problemer på en kreativ og innovativ måde med brugeren i fokus. Design Thinking har 6 faser: empathize, define, ideate, prototype, test og implementering.</p>
                
                <h4 class="h4boks">Divergens og konvergens</h4>
                
                <p class="tekst-i-boks">Inden vi dykker ned de 6 faser skal vi lige have styr på divergent og konvergent tænkning. Vi bruger begge metoder under Design Thinking processen. Det er en måde at hjælpe hjernen til at arbejde konstruktivt, i stedet for at modarbejde. (bilag 3.1)</p>
                
                <p class="tekst-i-boks">Divergent tænkning er når vi ønsker at brede ud, udvide vores horisont og udforske muligheder. Det typisk her hvor vi ide-genererer og ide-udvikler. Vi tænker her kvantitet over kvalitet. </p>
                
                <p class="tekst-i-boks">Konvergent tækning er når vi skal snævre noget ind. Vi vurderer og sortere, så vi kan beslutte hvad vil skal gøre. Her tænker vi kvalitet frem for kvantitet.  </p>
                
