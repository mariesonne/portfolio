            <h3 class="h3" id="fase2">Define</h3>
            
            
            <p class="tekst"> Vi skal i denne fase definere problemstillingen. Vi skal spørge os selv om hvad der er det egentlige problem. Det er vigtigt at alle deler deres forståelse. Dette kan gøres med illustrationer, Post-it, Plakater, Mind Maps, videoer, fotos osv. Der skal defineres hvad der skal arbejdes hen i mod. (ibid.) Dette er en konvergent fase, da vi indsnævrer.  </p>
            
            
           <h3 class="h3" id="fase3">Ideate</h3>
            
            
            <p class="tekst"> Nu må alle ideer komme på bordet – og først nu! Jo flere ideer, jo bedre. (ibid.) Ingen ideer må skydes ned, altså vi skal ikke stille spørgsmålstegn ved nogle ideer. Alle ideer er velkomne, både realistiske og urealistiske. Det gælder om at holde en kreativ og positiv energi. Vi kan bruge teknikker som brainstorm, omvendt brainstorm og lignende.   </p>
            
            
            <p class="tekst">Ved begge Design Thinking projekter har vi fået en favorit ide. Når man har fundet en ide, som alle synes er rigtig god, så er det svært at fokusere på flere ideer. Men det er rigtig vigtigt at man bliver ved med at idegenerere. Det er nemlig her man ofte får de rigtig gode og kreative ideer frem. Man kan evt. bruge nogle andre idegenereringsteknikker. </p> 
            
           
            <h3 class="h3" id="fase4">Prototype </h3>
            
            <p class="tekst">Vi skal i denne fase flytte vores ide fra ord til skitser. En prototype skal i denne sammenhæng forstås som en fysisk repræsentation af idé, som kan kommunikere udvalgte aspekter af idéen til relevante aktører med henblik på at få feedback. (ibid.) Nå vi fremviser en prototype af en ide, i stedet for bare at forklare om den så sikre vi at folk bedre forstår den. </p>
            
            <p class="tekst">Protypen gør det også nemmere at test ideen, så testpersonerne kan få noget håndgribeligt, altså lidt mere at forholde sig til end bare ord. Man kan både lave en low-fidelity prototype og en high-fidelity prototype. En low-fidelity er en meget simpel prototype, det kan f.eks. være en prototype af en hjemmeside, som bare er tegnet med blyant på papir. En high-fidelity prototype er en mere detaljeret prototype. Det kan f.eks. laves i Adobe XD, som er et program hvor man kan lave smarte prototyper, der næsten ligner det færdigproducerede resultat.  </p>
            
            <h3 class="h3" id="fase5">Test</h3>
            
            <p class="tekst">Man bør teste dine ideer så tidligt som muligt. På denne måde sparer vi ressourcer, da vi kan fortage rettelser tideligt i forløbet. Det er nemmere rette noget, som er skrevet med blyant end at skulle ændre en hel kode. (ibid.) Nogle gange undlader man at teste, måske fordi der i forvejen er pres på tide. Men dette bringer ofte negative konsekvenser. Vi bør derfor aldrig springe denne del over, da det handler om at få brugeren med ind over fra start. Dette kan gøres med en helt almindelig tænke-højt-test.  </p>
            
            <p class="tekst">Under projektet om de 17 verdensmål, besluttede jeg mig for at springe ud i at lave en high-fidelity prototype i Adobe XD. Vi lavede naturligvis først en papir-udgave. Vi besluttede os for at kun teste XD-prototypen. Vi fandt dog hurtigt ud af, at det også var nødvendigt at teste papirudgaven. Det skyldes at når vi testede XD prototypen, så var der mere fokus på det tekniske frem for ideen. </p>
            
            <h3 class="h3" id="fase6">Implementering</h3>
            
            <p class="tekst">Den sidt skal vi have produktet implementeret. Vi skal altså videre med det, så der er nogle der kan få gavn af vores gode ideer. Det kan f.eks. være en pitch af ideen eller en grundig gennemgang. </p>
            
