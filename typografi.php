            <section class="boks">
        
                <h3 class="h3" id="hvad">Hvad er typografi?</h3> 
                
                <p class="tekst-i-boks"><span id="citat">Typografi er et håndværk, der giver menneskets sprog visuel form.</span> <br> - Robert Bringhurst </p>
                
                 <p class="tekst-i-boks"> Typografi er vigtig for alle der arbejder med visuel kommunikation – og det er dermed også vigtig for multimediedesignere. Når vi arbejder med typografi, er der nogle spilleregler som vi bør kende. (Rold, Typografi, 2019)</p>

            </section>
            
            <h3 class="h3" id="typer">Skrifttyper</h3>
            
            <p class="tekst">De mest udbredte skriftyper er serif og sans serif. Derudover har man også en kategori til de skrifttyper der ikke går ind under en af de to andre kategorier. </p>
            
            <h4>Serif</h4>
            
            <p class="tekst">Serif er hvor bogstaverne har små ”fødder”. Nå man læser, kigger man ikke på hvert enkelt bogstav enkeltvis. (ibid.) Vi kiggerne på formen, som bogstaverne skaber når du danner et ord. Seriffer er med til at danne disse former, da den grupperer bogstaverne. Fødderne danner på en måde en form for hjælpelinje, så gør det rart for øjnene, når man skal læse meget tekst. Serif bruges ofte i dag, både i aviser og bøger, selvom det er designet for flere hundrede år siden. </p>
            
            <p class="serif">Et eksempel på en tekst med serif</p>
            
            <h4>Sans-serif</h4>
            
            <p class="tekst">Sans er fransk og betyder uden. (ibid.) Det er altså skrifttyper uden fødder. Bogstaverne har her samme tykkelse overalt. Dette giver et mere enkelt udtryk. Denne skrifttype er god til korte tekster der skal læses hurtigt. (ibid.) Sans serif skaber naturligvis ikke hjælpelinjen og skal derfor helst ikke bruges til meget teksttunge ting, som f.eks. bøger osv. </p>
            
            <p class="sans-serif">Et eksempel på en tekst med sans-serif</p>
            
            <h4>Andre skrifttyper</h4>
            
            <p class="tekst">De fleste skrifttyper vi bruger, falder ind under en af de to hovedkategorier. (ibid.) Men der er også skrifttyper som ikke gør. Dette kan eksempelvis være monospace-skrifter. Monospace-skrifter er hvor at alle bogstaver er lige brede – altså alle bogstaver fylder det samme. Det bruger vi eksempel når vi koder. Ved disse skrifter er det vigtigere at hvert bogstav fremgår tydeligt end at det er let læseligt. (ibid.) Et andet eksempel på en skrifttype, er script-skrifter. Disse teksttyper ligner håndskreven tekst.  </p>
            
            <p class="monospace">Et eksempel på en tekst med monospace-skrift</p>
            
            <p class="script">Et eksempel på en tekst med script-skrift</p>
            
            <p class="tekst">Det er vigtigt at vi vælger en skrifttype der passer til den fortælling og den tone, vi ønsker at formidle. Vi skal derfor overveje hvordan vi ønsker at udtrykke os inden vi vælger skrifttypen. </p>
            
            
            
            
           <section class="boks">
        
                <h3 class="h3" id="typefacevsfont">Typeface eller font</h3> 
               
                <p class="tekst-i-boks">Det er en meget almindelig fejl at komme til at blande typeface og font sammen. (Keung, 2020) Det har jeg i hvert fald også gjort i mange år, og gør det nok stadig en gang imellem. </p>

                
                <h4 class="h4boks">Typeface</h4>
            
                <p class="tekst-i-boks">Et typeface er en gruppe tegn, der deler et fælles DNA. For eksempel er Alegreya Sans en anden skrifttype end Rockwell Std. Det er egentligt typisk dette jeg ville kalde for en font. Men det hedder altså et typeface. </p>
               
                <h4 class="h4boks">Font</h4> 
               
                <p class="tekst-i-boks"> Font er f.eks. bold, italic osv. indenfor den samme typeface. Det er altså samme typeface men forskellige fonts.  </p>
        
            </section>
            
            <h3 class="h3" id="begreber">Typografiske begreber</h3>
            
            <h4>Minuskler </h4>
            
            <p class="tekst">Minusker er de små bogstaver. (bilag 2.3)</p>
            
            <h4>Majuskler</h4>
            
            <p class="tekst">Majuskler er de ‘STORE’ bogstaver. Vi kalder dem også blokbogstaver. (ibid.)</p>
            
            <h4>X-height</h4>
            
            <p class="tekst">Er højden på minuskel ‘x’, målt fra bundlinjen til toppen af bogstavet. (ibid.)</p>
            
            <h4>Leading </h4>
            
            <p class="tekst">Det er det samme som linjeafstand. Måles fra bundlinje til bundlinje. Typisk udregnes leading med 120- 130% af skriftstørrelsen. (ibid.)</p>
            
            <h4>Kerning </h4>
            
            <p class="tekst">Afstanden mellem hvert enkelt bogstav kan justeres, således der skabes balance. Dette er kerning eller knibning.  (ibid.)</p>
            
            <h4>Tracking </h4>
            
            <p class="tekst">Justerer den overordnede afstand mellem alle bogstaver i et ord, eller en længere markering af ord. (ibid.)</p>
            
            
            <h3 class="h3" id="pers">Personlig reflektion</h3>
            
            <p class="tekst">Jeg var ikke med til undervisningen om typografi, jeg har derfor lært om det, ved at læse i bogen "Interfacedesign". For det meste lærer jeg emner bedst til klasseundervisningen, men jeg føler at jeg alligevel fik ret godt styr på typografi. Dette emne er utrolig vigtigt. Jeg kan personligt godt lide de mere simple typefaces, som er sans-serif. Men jeg er nu opmærksom på, at ens personlige præferencer ikke altid passer til den fortælling der skal formidles. </p>
            
