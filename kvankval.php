                <p class="tekst-i-boks">Vi kommer ofte ud for at skulle indsamle oplysninger. Det kan være til et projekt eller lignende. Til at indsamle viden bruger vi nogle forskellige metoder. De kan deles op i kvantitative og kvalitative. (Busch, Kvantitativ og kvalitativ epiri, 2015) </p>
                

