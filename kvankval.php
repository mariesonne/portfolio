            <h3 class="h3" id="kvan">Kvantitativ emperi</h3>
            
            <p class="tekst">Kvantitative undersøgelser er metoder hvor vi finder det ”hårde data”. Det er altså f.eks. tal, svar eller facts som kan tælles og statistisk beregnes. (Harboe, 2020, s. 75) Vi bruger det til at undersøge hvor mange mennesker der gør noget bestemt. (Busch, Kvantitativ og kvalitativ empiri, 2015, s. 84) Vi har altså fokus på mængden af besvarelser, frem for den dybere mening. Det kan f.eks. være spørgeskemaer. </p>
            
            
            <p class="tekst">Der er flere fordele ved de kvantitative metoder. En af styrkerne ved de kvantitative undersøgelser er generaliserbarhed. (Harboe, 2020, s. 76)  Det skyldes at de ofte inkludere mange flere respondenter end ved de kvalitative metoder.  Derudover har vi meget let ved at få overblik over det store antal svar. (Langkjær, Holleufer , &amp; Bülow, 2015) Vi kan f.eks. sætte det ind i tabeller og diagrammer, som giver et meget godt overblik over resultatet af undersøgelsen. Derudover er de kvantitative metoder ikke så tidskrævende, hvilket gør at det er forholdsvis billigt at udføre. (ibid.) Dog er der også rigtig mange ulemper, når man gør brug af de kvantitative metoder. Et eksempel er, vi får meget korte svar, hvor vi ikke får nye informationer på samme måde som vi ville til et interview. (ibid.) Vi kan heller ikke løbende ændre i spørgsmålene, og af den grund bør man altid teste sit spørgeskema inden man starter undersøgelsen. (Harboe, 2020, s. 76) </p>
            
            
            <h3 class="h3" id="kval">Kvalitativ emperi</h3>
            
            
            <p class="tekst"> Hvor vi ved de kvantitative undersøgelser har fokus på hvor mange der gør noget bestemt, så har vi ved de kvalitative mere fokus på, hvorfor folk gør som de gør. (Busch, Kvantitativ og kvalitativ empiri, 2015, s. 88) Vi har her en mere undersøgende og udforskende tilgang. (Harboe, 2020, s. 77) Vi er ikke så generaliserende. Derimod ønsker vi nu, at få meninger, synspunkter og indtryk frem. Vi får altså en dybere forståelse for eller ny viden om de personer, vi undersøger. (Langkjær, Holleufer , &amp; Bülow, 2015) Vi arbejder her med ”blødt data”. Vi kan f.eks. gøre brug af gruppe interviews eller tænke-højt-tests. (Busch, Kvantitativ og kvalitativ empiri, 2015, s. 89-97)  </p>
            
            
            <p class="tekst">Det fyldige svar er blot en af de mange fordele ved de kvalitative undersøgelser. Vi får mulighed for at tilpasse undersøgelsen undervejs. (Harboe, 2020, s. 78) Det kan f.eks. være ved at tilpasse eller tilføje spørgsmål under et interview. På den måde får vi uddybet ønskede spørgsmål, som vi måske ikke havde et tænkt på før interviewets start. Dog er de kvalitative undersøgelser ikke generaliserbarer. Vi har typisk kun få respondenter inkluderet i undersøgelsen. (Harboe, 2020, s. 78) Vi kan derfor ikke få resultater der kan generaliseres til store befolkningsgrupper. Kvalitative undersøgelser ses ofte som meget subjektive. (Harboe, 2020, s. 85) Derudover er disse undersøgelser oftest meget mere tidskrævende, hvilket også gør dem dyrere at udføre. (Langkjær, Holleufer , &amp; Bülow, 2015) </p>
            
            
            
           <h3 class="h3" id="kombi">Kombinationer</h3>
            
            
            <p class="tekst"> Kvantitative og kvalitative metoder er ofte hinandens modsætninger. (Harboe, 2020, s. 81) Dog vil man ofte gøre brug af begge metoder, da den ene metodes muligheder er den anden metodes begrænsninger. Man kan f.eks. bruge en kvalitativ metode som forundersøgelse til den kvantitative undersøgelse. På den måde får man blandt andet bekræftet hvilke spørgsmål der skal stilles eller hvilke svarmuligheder skal der være. (ibid.) Vi får på denne måde struktureret og målrettet de kvantitative metoder.   </p>
            
            
            <p class="tekst">Ved en anden kombination starter vi med de kvantitative undersøgelser. (ibid.) Vi får derved et overblik før vi starter indsamlingen af den mere dybdegående kvalitative data. Før vi starter et interview, er der et par nødvendige spørgsmål, der skal besvares. Det kan f.eks. være hvor vi skal lægge fokus. (ibid.) Via statistikker fra spørgeskemaer kan vi muligvis få svar på disse spørgsmål. På denne måde giver de kvantitative metoder struktur til de eller meget ustrukturerede kvalitative metoder. (ibid.)</p> 
            
            <p class="tekst">Man kan altså med fordel kombinere undersøgelserne, da de supplerer hinanden godt. Dog er der også mange gode og vellykkede eksempler på undersøgelser der udelukkende bygger på enten kvalitative eller kvantitative metoder. (Harboe, 2020, s. 85) </p>
            
            
            
