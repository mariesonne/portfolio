                <h3 class="h3" id="at">At skrive tekster</h3> 
                
                <p class="tekst-i-boks">At skrive tekster er et håndværk. (Busch, Tektproduktion, 2015) Det vil sige, at det er noget alle kan lærer, og det er også noget alle kan blive gode til, jo mere man gør det. Når vi skriver tekster, kræves det at vi forstår målgruppen. Derudover skal man også overveje hvilket medie man skriver til. Vi skriver ikke de samme i en artikel som på de sociale medier. (ibid.) Jeg vil komme ind på følgende medietyper:</p>
                
                <p class="tekst-i-boks">Artiklen</p>
                <p class="tekst-i-boks">Brochuren </p>
                <p class="tekst-i-boks">Annoncen</p>
                <p class="tekst-i-boks">Tekster til web</p>
                <p class="tekst-i-boks">Sociale medietekster</p>
            
                <p class="tekst-i-boks">Det er vigtigt at forstå de forskellige mediers egenskaber i forhold til hvad teksten kan som kommunikationsmiddel. (ibid.) Vi kan bruge enkle metoder til at skabe effektiv kommunikation. </p>
                
            </section>
            
            <h3 class="h3" id="king">Content is king!</h3>
            
            <p class="tekst">Content betyder indhold. Det kan være indhold på en hjemmeside eller være et opslag på Facebook. Men man må ikke undervurdere indholdet. Indholdet er lige netop det, der giver værdi for kunderne. (bilag 4.2) Typografi, videoer og grafik er også vigtigt, når vi skaber godt indhold på de sociale medier, men med de tekstlige midler kan man få teksten til at tale på en bestemt måde til netop den målgruppe man ønsker at ramme. (Busch, Tektproduktion, 2015) Men før vi skaber det gode indhold, er det meget vigtigt at have en strategi.</p>
            
            <section class="boks">
        
                <h3 class="h3" id="begreber">Journalistiske grundbegreber</h3> 
                
                <p class="tekst-i-boks">Det er en god idé at læse eller genlæse følgende begreber, for at være godt klædt på til at skrive gode tekster. </p>
                
                <h4 class="h4boks">Nyhedskriterier</h4>
                
            
                <p class="tekst-i-boks"><span class="teksttyk">Væsentlighed</span> dvs. noget der har grundlæggende betydning eller konsekvens for mange mennesker. </p>
                <p class="tekst-i-boks"><span class="teksttyk">Sensation </span> dvs. noget der fascinerer eller chokerer. </p>
                <p class="tekst-i-boks"><span class="teksttyk">Konflikt </span> dvs. når sager eller personer er på kollisionskurs.</p>
                <p class="tekst-i-boks"><span class="teksttyk">Identifikation </span> dvs. noget som læseren kan genkende sig selv i. En historie, der får læseren til at tænke ’Det kunne være mig’, ’Bare det var mig’ eller ’Godt, det ikke er mig’</p>
                <p class="tekst-i-boks"><span class="teksttyk">Aktualitet </span> dvs. noget der er oppe i tiden (Bilag 4.2) </p>
                
                <p class="tekst-i-boks">Hvis i tekst lever op til mange af nyhedskriterierne er den meget relevant for målgruppen. (Busch, Tektproduktion, 2015) Det vil altså sige at man skal kende ens målgruppe godt, for at kunne vurdere de fem nyhedskriterier. Hvis vi kender vores målgruppe og gør brug af nyhedskriterierne, så får vi kvalitet, altså bedre indhold, som rammer vores målgruppe og derved skaber værdi. (bilag 4.2) Vi får delinger, altså folk der deler links til f.eks. vores webside, men det kan også være delinger på de sociale medier. Derudover får vi også mere trafik, fordi folk har delt f.eks. vores webside. </p>
                
                <h4 class="h4boks">Inddeling af stof</h4>
                
                <p class="tekst-i-boks">Man kan inddele sit indhold i tre kategorier: Oplysning/information, debat og underholdning </p>
                
                <p class="tekst-i-boks">Man kan også underinddele kategorierne efter hvor høj grad der er tale om en objektiv tekst eller en subjektiv tekst. (Busch, Tektproduktion, 2015) Et eksempel på noget objektivt kunne være en reportage og et eksempel på noget meget subjektivt kunne være en kommentar eller et læserbrev. Hvis en tekst er meget objektiv, er der et højere krav til at, det der står, er sandt. Det kan f.eks. være åbningstider til en butik. Dette er en objektiv tekst uden egne holdninger. Hvis en tekst er mere subjektiv, så er der et mindre krav til, at det der står, er sandt. Det er her ens egene holdninger og meninger skinner igennem.  </p>
                
                <h4 class="h4boks">Kilder</h4>
                
                <p class="tekst-i-boks">Når man producerer tekster, kommer man sommetider til at beskæftige sig med kilder. (ibid.) Man skal her huske at forholde sig kildekritisk. </p>
                
                <p class="tekst-i-boks">Man skal passe på med at bruge kilder man ikke kan tjekke. Det kan f.eks. være andenhånds kilder eller kilder uden afsender, som man f.eks. finder på Wikipedia. </p>
                
                <p class="tekst-i-boks">Man skal også passe på med at bruge kilder man har fælles interesser med. Det er vigtigt at se bort fra, hvad man selv mener. Se på tingene fra andre perspektiver, og få helheden med. </p>
                
                <p class="tekst-i-boks">Til sidst skal man passe på kilder man godt kan lide. Det er nemlig svært at forholde sig kritisk, hvis man har en personligt forhold til kilderne. Derved bliver teksten man skriver mere subjektiv og dette kan være uhensigtsmæssigt i forhold til formålet med teksten. (ibid.)</p>
                
                 <h4 class="h4boks">Opsætning</h4>
                
                <p class="tekst-i-boks">Når man taler om selve tekstens opbygning bruger man nogle fagtermer. (ibid.) Herunder vil jeg forklare om nogle af de mest væsentlige.</p>
                
                <h5>Rubrik</h5>
                
                <p class="tekst-i-boks">Rubrikken er det som andre ville kalde overskriften. Den kan øge nysgerrigheden, men samtidig dække over indholdet. (ibid.) Overskriften skal fange opmærksomheden hos læseren. Den skal være kort og sand. Nogle bruger også en underrubrik, altså en underoverskrift. (ibid.) Den kan bruges til at folde overskriften ud. </p>
                
                <h5>Trompet</h5>
                
                <p class="tekst-i-boks">Trompeten en overlinje. Den skal ses som en form for vejviser. (ibid.) Den bruges til at fortælle hvad artiklen handler om. Den kan f.eks. fortælle hvor i verdenen det finder sted. </p>
                
                <h5>Manchet</h5>
                
                <p class="tekst-i-boks">Manchetten er et par linjer under overskriften der fungerer som et referat af hele teksten. (ibid.) Manchet og underrubrik kan nemt forveksles. Manchetten er større end underrubrikken, men grænsen imellem dem er flydende. (ibid.)</p>
                
                <h5>Byline</h5>
                
                <p class="tekst-i-boks">En byline fortæller hvem der har skrevet teksten eller f.eks. hvem der har taget et billede. </p>
                
                <h5>Kolofon</h5>
                
                <p class="tekst-i-boks">En kolofon indeholder information om mediet. Det kan være hvem der er den ansvarshavende redaktør. Det kan også være kontaktoplysninger i form af telefonnumre og adresse. (ibid.) </p>
                
            </section>
            
            
            <h3 class="h3" id="artikel">Artiklen</h3>
            
            
            <p class="tekst">Den klassiske artikel er en betegnelse for en kort, afsluttet, selvstændig behandling af et emne. (ibid.) Vi kan finde artikler både i aviser og tidskrifter, men også online. De kan være fagligt tematiseret, nyhedspræget eller skrevet ud fra en personlig vinkel. Ved alle slags artikler går det ud på at skabe interesse ved at give læseren et hurtigt overblik over indholdet. I en artikel skal læserene ikke nødvendigvis læse alt, for de kommer frem til en konklusion. Tvært i mod, må konklusionen gerne komme tideligt i en artikel. </p>
            
            <img src="billeder/nyhedstrekant.png" width="90%">
            
            <p class="tekst"> Her ses nyhedstrekanten. I nyhedstrekanten står det mest vigtige først og det mindst vigtige til sidst.</p>
            
            <p class="tekst">Nyhedstrekanten bruges primært til de mere objektive nyheder. Hvis man skal skrive en mere subjektiv artiklen, bruges kommodestrukturen oftest. (ibid.) En kommode består af en ramme og så nogle skuffer man kan trække ud, hvis man har lyst til det. Der er en top og en bund, som er fast. Teksten består af en begyndelse og en slutning som indrammer mindre delfortællinger. (ibid.) De små delfortællinger ligger nede i skufferne, som man selv vælger om man ønsker at åbne eller bare springe over.</p>
            
            
            <p class="tekst">Hvis vi ikke har en nyhedsvinkel på artiklen, kan vi bruge hey-you-see-so-modellen til at opbygge den. Det kan f.eks. være hvis vi skal skrive en produktartikel.   </p>
            
            <p class="tekst"><span class="teksttyk">HEY</span> fang læsernes opmærksomhed. Som i HEY! Det er spændende det her. </p>
                <p class="tekst"><span class="teksttyk">YOU </span> Gør det nærværende: Det er dig, læser, det handler om. </p>
                <p class="tekst"><span class="teksttyk">SEE </span> Se konsekvenserne, det betyder faktisk noget for dig</p>
                <p class="tekst"><span class="teksttyk">SO </span> Perspektiv og konklusion. Det fører dig til at… </p>

            
             <h3 class="h3" id="brochure">Brochuren</h3>
            
            <p class="tekst">Når man skriver indholdet til en brochure, så skal det struktureres meget grundigt. (ibid.) Det er vigtigt at man tænker over typografi, billeder og tekst i sammenhæng som indholdet. De tre elementer får brochuren til at fremstå troværdig i forhold til budskabet. Herunder er der et eksempel på hvordan man kan opbygge sin brochure. Eksemplet er taget udgangspunkt i en lampeforretning. </p> 
            
            <p class="tekst">Overordnet formål: At sælge lamper </p>
            <p class="tekst">Målgruppe: Højere middelklasse og opefter </p>
            <p class="tekst"> Produkt/emne: Brochuren skal positionere lampedesignet</p>
            <p class="tekst">Stilvalg for tekst og visuelt design: Ønsket er at sælge stemning og vise lampernes eksklusivitet og elegance i omgivelsre, som tiltaler målgruppen </p>
            <p class="tekst"> Medieformatets detaljer: Formatet er fire A5-sider i farve</p>
            <p class="tekst">Distribution: Brochuren skal lægges synligt i lampeforretningen </p>
            <p class="tekst"> Informationsarkitektur: Forside: Flot billede, der sælger drømmen. Tydeligt logo, men ikke for stort og dominerende. Side 1 og 2: Når man åbner brochuren, skal der være flotte billeder med produkterne samt gode tekster, der ikke kun informerer om priser med også appellere følelsesmæssigt til kunderne. Bagside: Kontaktinformationer på butikken, webshoppen og servicemuligheder, altså telefon og information om chat-service. </p>
            
            <p class="tekst">Man skal altså definere strategi, målgruppe informationsarkitektur osv. først og derefter skrives tekstens dele. Kort er godt når vi skriver tekster til brochure.  </p>
            
            <h3 class="h3" id="annonce">Annoncen</h3>
            
            <p class="tekst">Når vi skriver annoncer, skal vi tale hurtigt og præcis til vores målgruppe. (ibid.) Overskriften skal være fængende, så man lægger mærke til den, når man bladrer til en avis eller går forbi et bustoppested. Den skal være rettet mod målgruppen, så man får de rigtige til at stoppe op og læse videre. Motiv, billeder eller grafik skal fungere som en bekræftelse af overskriften eller f.eks. som en ironisk kommentar til den. (ibid.)  Brødteksten i en annonce skal være meget kort, som kun indeholder det mest basale. Logoet er også synligt på en annonce. Der er ofte også et payoff, altså et lille slogan. Til sidst skal der også være en pligttest. Her står typisk om forbehold og begrænsninger i et tilbud, copyright-informationer osv. Man bruger altså både tekst- og billedsprog til at tale direkte til målgruppen. Til at lave en annonce kan man bruge AIDA-modellen. </p> 
            
            <img src="billeder/aida.png" width="90%">
            
            <p class="tekst">A står for attention. Vi skal altså først og fremmest skabe opmærksomhed. Det kan være billedet der skaber opmærksomhed, men det kan også være en god overskrift. (ibid.) I står for interest. Mange annoncer kan skabe opmærksomhed, men det er ikke alle der er interesseret. D står for desire, altså at ønske. Hvis målgruppen er interesseret i annoncen, kan det være at den får dem til at ønske, at de havde produktet. A står for action. Til sidst skal man have læseren til at gøre noget. Det kan være at købe produktet, men det kan også være noget helt andet, som f.eks. at donere blod. </p>
            
            <h3 class="h3" id="web">Tekster til web</h3>
            
            <p class="tekst">Når man skriver tekster til web, handler det i høj grad om læserens opmærksomhed. (ibid.) Det er ikke længere nok med en fængende og klar artikel. Der skal også være links til det læseren søger. Folk læser sjælendt grundigt på nettet, de scanner siden hurtigt og finder det der er relevant for dem. Derfor skal man splitte sin webtekst op i korttekst og heltekst. (ibid.) </p>
            
            <p class="tekst"> Korttekst er web-tekstens nøgleord. Det kan evt. være en titel med et billede og et kort resume af artiklen. </p>
            
            <p class="tekst"> Helteksten er det vi kommer ind til, når vi klikker videre fra kortteksten. Som sagt scanner folk oftest en side og læser kun det de finder relevant. Det er derfor vigtigt, at siden er nem at navigere rundt på. (ibid.) Dette kan gøres ved at dele helteksten ind i små emner. Så hver enkel del af teksten skal have sin egen lille overskrift og sin afrunding. Man må aldrig gå ud fra, at læseren har læst alt teksten på siden. Man skal derfor lade vær med at referere til andet tekst på siden. </p>
            
            <p class="tekst">Brug links i tekster. (ibid.) De skal være præcise. Man skal ikke skrive ”se mere her”. Folk ved godt hvordan links fungere. Læseren spotter dem med det samme. </p>
            
            
            <p class="tekst">Vær bevidst om følgende:  </p>
            
            <p class="tekst"><span class="teksttyk">Modtager: </span> Hvem skal vide hvad?. </p>
            
            <p class="tekst"><span class="teksttyk">Afsender:</span> Hvad er mine mål. Hvordan målretter jeg mit sprog og mine budskaber til brugeren? Undgå lange kludrede sætninger. (ibid.)</p>
       
             <p class="tekst"><span class="teksttyk">Formål og mål:  </span> Hvad er formål med websiden? Hvilke mål har jeg for besøg på sites specifikke sider. (ibid.)</p>
            <p class="tekst">Det er også vigtigt at linke til eksterne sider, og overveje hvem der kan linke til ens egen side. Derudover er det også vigtig at overveje hvilke informationskrav brugerne stiller. Det kan f.eks. være links til sociale medier.</p>
            
