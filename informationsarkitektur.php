                <h3 class="h3" id="hvad">Hvad er informationsarkitektur?</h3>    

                <p class="tekst-i-boks">Vi har ofte flere sider med information på vores website, medmindre vores hjemmeside er en One Page-website. (Busch, 2015) Informationsarkitektur handler om at organisere de forskellige undersider. Vi kan sammenligne det med en arkitekt, der tegner huse og bygninger og beregner stabilitet og lignende. Det er ikke arkitekten der bygger det. Sådan kan vi også se informationsarkitektur. Vi skal have fokus på hvad formålet med siden er, hvem der skal bruge siden og selvfølgelig hvordan navigationen skal være. (ibid.) Der er IKKE her der skal tænkes design eller kodning ind! </p>
             
                <p class="tekst-i-boks">Målet med informationsarkitektur for websites er at opbygge et hierarki af menuer og indholdssider, der føles naturligt for brugerne. (ibid.) Brugerne giver hellere op end at skulle lede rundt på en hjemmeside, og det er derfor dette er så vigtigt. Det handler derfor om at gøre det så brugervenligt som muligt. Vi skal hjælpe brugeren hen til sit mål på en effektiv måde. Se derfor hjemmesiden, fra brugernes øjne.</p>
             
            </section>
            
            <h3 class="h3" id="latch">LATCH</h3> 
        
            <p class="tekst">LATCH er fem måder at organiserer på. LATCH står for Location, Alphabet, Time, Category og Hierarchy. Det er oprindeligt inddelt af Saul Wurman. (Bilag 1.8)  </p>
        
            <h4>Location</h4>
            
            <p class="tekst"> Med denne teknik organiserer vi informationerne i forhold til placering. Kort er typisk organiseret efter placering. Det kan f.eks. være et metrokort som ses herunder. Kortet er organiseret efter hvilke områder metroen kommer igennem. Hvis man f.eks. organiseret det i alfabetisk rækkefølge ville kortet være misvisende. </p>
        
            <img src="billeder/metrokort.png" width="80%">
        
            <h4>Alphabet</h4>
            
            <p class="tekst"> Organisering af indholdet alfabetisk er nok en af de nemmeste måder at gruppere information på, hovedsageligt fordi alfabeter er universelt anerkendte. Dog skal man passe på, at man ikke forvirrer brugeren. Det kunne f.eks. være i en opskriftsbog, hvor brugeren ønsker at finde opskriften på en stærk lasagne. Hvis ”Stærk lasagne” skulle placeres korrekt i forhold til alfabetet, så skulle den være under S, og der vil brugeren højest sandsynligt ikke lede. </p>
        
            <h4>Time</h4>
            
            <p class="tekst"> At organisere efter tid er en meget smart på f.eks. de sociale medier. De fleste går ikke ind på deres Instagram app for at se opslag i alfabetisk rækkefølge. De sociale medier bruges generelt til at blive opdateret med det seneste nye, og vi ønsker derfor ikke at se gamle opslag, derfor er det smart at organisere efter tid. Et andet eksempel er busplaner. Busplanerne er også organiseret efter tid. Hvis det ikke var det, skulle vi bruge meget lang tid på at finde ud af hvornår bussen er i de forskellige byer. </p>
        
            <img src="billeder/busplan.png" width="80%">
        
            <h4>Category</h4>
            
            <p class="tekst"> At organisere efter kategorier er en af de måder der oftest bliver brugt på hjemmesider. Når vi organiserer efter kategorier, er det vigtig at de kategorier vi definerer, giver mening for brugeren. Man kan organisere ting i næsten uendeligvis af kategorier – tænk bare på alle de kategorier man kunne lave til ens mad osv. i køkkenet. Farver, tørvarer, udløbsdatoer osv.  Vi kan bruge kortsorteringstestmetoden til at inddele vores undersider i kategorier ud fra brugerens tanker.</p>
        
            <p class="tekst"> Ved kortsortering skriver man alle undersider ned på små kort. Der er to versioner af kortsortering. I den ene lader man testdeltagerne inddele undersiderne i kategorier de selv bestemmer. I den anden version er der allerede bestemt overkategorier og brugeren skal placere undersiderne der hvor han eller hun synes det giver bedst mening at de er placereret.  Det er vigtigt at man får testdeltageren til at forklarer de overvejelser og tanker der ligger bag resultatet. Her kan man også få et indblik i, om testdeltageren synes nogle ord skulle være andrelede og lignende. (ibid.) </p>
        
             <h4>Hierarchy</h4>
            
            <p class="tekst"> Ved at organisere efter hierarki kan vi arrangere vores information efter f.eks. ny til gammel, lav pris til høj pris osv. Dette ser vi ofte når vi shopper. Det kan eksempelvis være hvis vi ønsker at se det billigste, så kan vi som regel vælge at vi vil se det fra lav til høj pris. </p>
        
            
