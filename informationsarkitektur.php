            <img src="billeder/lapper.JPG" id="lapper">
            
             <p class="tekst"> Jeg har hovedsageligt lært om informationsarkitektur i undervisningen. Men det med at organiserer ting så de er der hvor man leder efter dem, gør de fleste egentligt. Det kan f.eks. være mine strømper der ligger i en skuffe i mit tøjskab. Jeg har lagt dem der, fordi at de der ligger smartest for mig. Det ville for mig og de fleste andre være ulogisk hvis de lå ude i køkkenet. Dette skal man blot overføre til websider og så forstår man hvordan informationsarkitektur fungere.</p>
        
