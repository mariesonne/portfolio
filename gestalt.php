                <h3 class="h3" id="principper">Designprincipper</h3> 
                
                <p class="tekst-i-boks">Først og fremmest, så kalder vi dem principper i stedet for love. Disse designprincipper hjælper os med at skabe struktur i vores design i forhold til hvordan mennesker opfatter visuelle objekter i forhold til hinanden. </p>
                
                 <p class="tekst-i-boks"> Designprincipperne kan både stå alene, men man kan også sagtens bruges sammen. Det er meget sjældent et designprincip står alene og vi arbejder ofte med flere end en. </p>

            </section>
            
            <h3 class="h3" id="fg">Figure-gound</h3>
            
                <p class="tekst">Som mennekser ser vi altid på elementer son enten at være i forgrunden eller i baggrunden. (bilag 2.4)Figure er det der er i forgrunden. Det kan sommetider være svært at adskille fra baggrunden. Som udgangspunkt er den største flade ground, men nogle gange snyder det.  Vi kan kigge efter kontraster, farver, bokse, skygger osv. når vi skelner mellem figure og ground. Der er ikke nødvendigvis kun én ”figure”. </p>

                <img src="billeder/figureground.png">
            
            <h3 class="h3" id="ws">White Space</h3>
            
                <p class="tekst">White Space kan buges som der ses i eksemplet her til at skabe fokus på en funktion, element eller lignende. Dette er noget vi kender fra søgemaskinen Google, som gør brug af meget White space på deres forside. Her er deres søgefunktion i høj grad i fokus, fordi der ikke er andet at se.  </p>

                <img src="billeder/whitespace.png" id="pricipper-billeder">
            
                <p class="tekst">White space bruges også skabe et klart visuelt hierarki og lave balance i et kaos. Som vi kan se i det første eksempel, står elementerne helt ubalanceret og i et stor rod. Vi kan bruge White space til at skabe et mere roligt udtryk og dermed gøre det mere balanceret. Dette ses i eksemplet herunder.</p>

                <img src="billeder/balance.png" id="pricipper-billeder">
            
            <h3 class="h3">Lighed</h3>
            
                <p class="tekst">Figurer eller elementer der ser ens ud, vil blive opfattet som om de er relateret til hinanden eller som en del af samme gruppe. (Rold, Fem vigtige gestaltlove, 2019) Det kan f.eks. være at de har samme form, størrelse eller farve. Selvom elementerne er langt fra hinanden, vil man stadig gruppere dem, hvis de er ens. (ibid.)  </p>

                <p class="tekst">Når vi ønsker at skabe lighed på en side, så er farver et rigtig stærkt redskab. Vi giver f.eks. ofte links samme tekstfarve og de bliver derfor relateret til hinanden. </p>

                <img src="billeder/lighed.png" id="pricipper-billeder">
            
            <h3 class="h3">Nærhed</h3>
            
                <p class="tekst">De elementer der star tæt på hinanden vil blive opfattet som relateret til hinanden. (ibid.) Nærhed slår lighed, så selvom der er flere forskellige former og farver, så ser vi dem stadig som en gruppe, hvis bare de står tæt. Vi kan sagtens bruge flere principper sammen.</p>

                <img src="billeder/naerhed.png" id="pricipper-billeder">

            <h3 class="h3">Kontinuitet</h3>
            
                <p class="tekst">De elementer der står så de danner en linje eller en kurve ses også som en gruppe. (ibid.) Dette bruger vi ofte i menulinjer, krummestier osv. </p>

                <p class="tekst">I dette eksempel danner elementerne en usynlig linje i bunden og de ses derfor som relaterede til hinanden, også selvom de hat forskellige farver og former.</p>

                <img src="billeder/kontinuitet.png" id="pricipper-billeder">
            
            <h3 class="h3">Lukkethed</h3>
            
                <p class="tekst"> De elementer der er ”lukket” inde i samme indramning vil ses som relateret til hinanden. Det er lige meget om de har samme form eller farve, man vil stadig gruppere dem når de er indrammet. (ibid.)</p>
            
                <img src="billeder/lukkethed.png" id="pricipper-billeder">

            <h3 class="h3">Figurdannelse</h3>
            
            <p class="tekst">Elementer som egentlig er separate, kan samlet danne en ny figur, og man ser dem derfor som en gruppe. (ibid.) </p>
            
            <p class="tekst">Det kan være meget tydeligt, som i eksemplet herunder viser. Der er dannet en tydelig firkant ud af firkanter. Det kan også være mere usynligt, som den trekant der dannes imellem ”cirklerne”.  </p>
            
            <img src="billeder/figurdannelse.png" id="pricipper-billeder">
            
            
            
