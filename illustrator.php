                <h3 class="h3" id="illustrator">Hvad er Illustrator?</h3>    

                <p class="tekst-i-boks">Illustrator er et Adobe program, som er uundværligt for mange grafikere. (Softworld-DK, 2016) Illustrator har mange forskellige værktøjer til både tegning, tekst og typografi. Noget af det Illustrator er mest kendt for, er alt lave vektor baseret grafik. (ibid.) Det vil altså sige at vi har mulighed for at skalere til store størrelser uden at tabe kvalitet. </p>
                
                <br><br>
                
                <p class="tekst-i-boks">Vektorgrafik er hvor illustrationerne er opbygget af streger og kurver, hvor at ved bitmap grafik er opbygget som et gitter med en masse firkanter – altså pixels.</p>

            </section>
            
            <h3 class="h3" id="tools">Tools</h3>    
        
                <h4>Selection tool</h4>
                
                <p class="tekst">Den markerer helt objektet eller grupper. Vi kan med dette værktøj bl.a. flytte vores objekter rundt og skalere deres størrelse.</p>
            
                <iframe src="https://giphy.com/embed/UeJcnGHgK2UdMkruK9" width="480" height="291" frameBorder="0" class="giphy-embed" allowFullScreen></iframe><p><a href="https://giphy.com/gifs/UeJcnGHgK2UdMkruK9"></a></p>

                <br><br>
            
            
                <h4>Direct Selection tool</h4>
                
                <p class="tekst">Dette værktøj er en af de vigtigste. Med direct selection tool. Vi kan her markere dele af et objekt. Vi kan også vælge ankerpunkter og redigere dem. Vi kan altså forme en figur, helt som vi ønsker.</p>
            
                <iframe src="https://giphy.com/embed/7QrQfswp9b047H5S9f" width="480" height="291" frameBorder="0" class="giphy-embed" allowFullScreen></iframe><p><a href="https://giphy.com/gifs/7QrQfswp9b047H5S9f"></a></p>

                <br><br>
            
            
                <h4>Pen tool</h4>
                
                <p class="tekst">Med Pen tool kan man skabe figurer ved at stilføje ankerpunkter og kurver. Man kan også tilføje ankerpunkter til figurer, så man kan med direct selection tool justerer formen.</p>
            
                <iframe src="https://giphy.com/embed/ytoU39eN5rZTG95zW6" width="480" height="291" frameBorder="0" class="giphy-embed" allowFullScreen></iframe><p><a href="https://giphy.com/gifs/ytoU39eN5rZTG95zW6"></a></p>

                <br><br>
            
            
                <h4>Line Segment tool</h4>
                
                <p class="tekst">Med dette værktøj kan man lave lige linjer med blot et museklik. </p>

                <iframe src="https://giphy.com/embed/JOjQhSTf6ckI0ehnXO" width="480" height="291" frameBorder="0" class="giphy-embed" allowFullScreen></iframe><p><a href="https://giphy.com/gifs/JOjQhSTf6ckI0ehnXO"></a></p>
            
                <br><br>
            
            
                <h4>"Shape" tools</h4>
                
                <p class="tekst">Rectangle tool, Ellipse tool, Polygon tool og Star tool, er hvad jeg putter under ”shape tools”. Men Rectangle tool kan vi laver firkanter. Med Ellipse tool laver vi cirkler.</p>

                <p class="tekst">Hvis vi holder shift inde når vi laver vores firkanter og cirkler, så få vi forme der har samme bredde og højde.</p>
            
                <p class="tekst">Hvis vi holder command inde, så bliver punktet hvor vore markør er til centrum – vi trækker altså ud fra centrum i stedet for fra et hjørne.</p>
            
                
                <iframe src="https://giphy.com/embed/c6W7wKNBxKDViA6tIU" width="480" height="276" frameBorder="0" class="giphy-embed" allowFullScreen></iframe><p><a href="https://giphy.com/gifs/c6W7wKNBxKDViA6tIU"></a></p>
            
            
                <p class="tekst">Med Polygon tool laver vi trekanter, firkanter, fem kanter osv. Med Star tool kan vi lave stjerne, hvor vi selv kan vælge hvor mange ”hakker” stjernen skal have.</p>
            
                <p class="tekst">Hvis vi bruger piletasterne når vi skaber vores stjerner eller polygoner, så kan vi justere antallet af kanter ved polygonerne eller antallet af ”hakker” ved stjernerne. </p>
            
                <iframe src="https://giphy.com/embed/U80SLZN7FDO3CcKPnu" width="480" height="276" frameBorder="0" class="giphy-embed" allowFullScreen></iframe><p><a href="https://giphy.com/gifs/U80SLZN7FDO3CcKPnu"></a></p>
            
                <br><br>
            
            
                <h4>Type tool</h4>
            
                 <p class="tekst">Vi burger Type tool til at lave tekst. Vi kan enten bare skrive ud af en lang linje eller markere en boks teksten skal holde sig indenfor. Vi kan vælge flere forskellige skrifttyper, størrelser osv. <a class="henvisning" href="typografi.php">Læs mere om typografi.</a>  </p>
            
                <iframe src="https://giphy.com/embed/adlDJPJRdXfeAeyUz5" width="480" height="291" frameBorder="0" class="giphy-embed" allowFullScreen></iframe><p><a href="https://giphy.com/gifs/adlDJPJRdXfeAeyUz5"></a></p>
            
                
                <br><br>
            
            
                <h4>Gradient tool</h4>
            
                 <p class="tekst">Med Gradient tool kan vi justere hvilke farver vores gradient skal indeholde. Vi kan også justere vinklen på gradienten.</p>
            
                <p class="tekst">De to mest brugte gradients er linear gradient og radial gradient som ses her:</p>
            
                <img id="gradient" src="billeder/gradient.png">
            
                <br><br>
            
            
                <h4>Shape Builder tool</h4>
            
                 <p class="tekst">Et super vigtig tool er Shape Builder tool. Man kan med dette værktøj sætte figurer sammen og skabe helt nye figurer. </p>
            
                
            <h3 class="h3" id="tools">Ikoner</h3>
            
                <p class="tekst">Ikoner er én af de mange ting man bruger Illustrator til. En af vores første opgaver var at lave en række ikoner. Jeg havde haft meget besvær med at bruge clipping masks, også selvom jeg kender det fra Photoshop. Så jeg besluttede, at det var noget jeg skulle gøre brug af, da jeg lavede mine ikoner – og det er denne måde jeg lærer funktioner at kende i programmer som Illustrator.</p>
            
