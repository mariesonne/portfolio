            <section class="boks">
        
                <h3 class="h3" id="bmc">Business Model Canvas</h3> 
                
                <p class="tekst-i-boks">Business Model Canvas er en enkel og simpel model der giver et overblik over virksomhedens forretningsmodel. Man kan bruge denne model til at udvikle nye forretningsmodeller. Men man kan også bruge den til at få et overblik over en allerede eksisterende forretningsmodel.</p>
                
                <img src="billeder/bmc%20copy.png" width="80%">
                
            </section>
            
            <h3 class="h3" id="kunde">Kunderne</h3>
            
            <p class="tekst">Her noterer man de vigtigste kunder, altså kernesegmentet. Kunderne er kernen for en hver forretningsmodel, da virksomheder kan ikke overleve ret længe uden kunder. (Osterwalder &amp; Pigneur, 2012) En virksomhed kan have flere store eller små kundesegmenter og de må tage et bevidst valg om, hvilke segmenter de vil henvende sig til. </p>
            
            
            <h3 class="h3" id="værdi">Værdifaktorer</h3> 
            
            <p class="tekst">Værdifaktorer er de produkter eller ydelser, som skaber værdig for kunderne. Det grunden til at kunderne henviser sig til virksomheden. Værdifaktorerne er dem der opfylder kundernes behov eller løser deres problemer. (ibid.)  Hvad er det for en værdi virksomheden giver kunden, der gør at de vil købe det. </p>
            
            
            
            
            <h3 class="h3" id="vej">Vejen til kunderne</h3>
            
            <p class="tekst">Her kigger vi på hvordan virksomheden kommunikerer med sine kunder og hvordan de når til dem. (ibid.) Vi kigger herunder på kommunikationskanaler, distributionskanaler og salgskanaler. Vi har her fokus på mange forskellige ting. Vi ser på hvordan virksomheden øger kundernes opmærksomhed på produkterne eller ydelserne, hvordan virksomheden gør det muligt for kunderne at købe produkterne og hvordan de yder kundesupport efter købet. (ibid.) </p>  
            
            
            
            <h3 class="h3" id="relation">Kunderelationer</h3>
            
            <p class="tekst">Vi ser her på hvilken type relation virksomheden har med sine kunder. Relationerne kan spænde fra personlige til automatiserede. (ibid.) Relationerne er vigtige for virksomheden, fordi det er ofte det der gør, at man fastholder sine kunder. Derudover kan man også skabe merslag på med gode kunderelationer. </p>
             
            
            <h3 class="h3" id="indtægter">Indtægter</h3>
            
            <p class="tekst">Indtægter er de penge virksomheden får ind fra kunderne. Hvis kunderne ses som hjertet i en krop, så skal indtægterne ses som arterierne. (ibid.) Indtægterne er altså også livsnødvendigt for en virksomhed. Vi kan her se på hvor meget de forskellige kundesegmenter er villige til at betale for værdifaktorerne og hvordan foretrækker at betale.  </p>
            
            <h3 class="h3" id="indtægter">Ressourcer</h3>
            
            <p class="tekst">Ressourcer beskriver de vigtigste aktiver, der er nødvendig for at forretningsmodellen skal fungere. (ibid.) Det er ressourcerne der gør det muligt for virksomheden at udarbejde deres produkter eller ydelser.(ibid.) Ressourcer kan være fysiske, økonomiske, immatrielle eller menneskelige. Fysiske ressourcer er f.eks. produktionsanlæg, køretøjer, systemer osv.  Immatirelle ressourcer kan være patent og cpyright, kundedatabaser osv. Menneskelige ressourcer er vigtige indenfor videns tunge og kreative brancher. (ibid.) Det kan være erfarne videnskabsfolk.  Økonomiske ressourcer er kontanter, kreditmuligheder og lignende. (ibid.) </p>
            
            <h3 class="h3" id="indtægter">Aktiviteter</h3>
            
            <p class="tekst">Her kigger vi på hvilke aktiviteter en virksomhed skal have for at få sin forretningsmodel til fungere. (ibid.) Det er handlinger som en virksomhed må udføre for at få succes. Det kan være aktiviteter der kræves for at skabe værdi eller aktiviteter der skal til for at forbedre kunder relationer. </p>
            
            
            <h3 class="h3" id="indtægter">Partnere</h3>
            
            <p class="tekst">Her kigger vi på hvilke partnere og leverandører en virksomhed har for at få forretningsmodellen til at fungere. Vi kan her se på hvilke partnere og leverandører der er de vigtigste. Derudover kan vi også se på hvilke ressourcer der købes af dem og hvilke aktiviteter de udføre. </p>
            
            
            <h3 class="h3" id="indtægter">Omkostninger</h3>
            
            <p class="tekst">Her ser vi på alle de omkostninger der er forbundet med at drive forretningsmodellen. (ibid.) At skabe og levere værdi, oprette kunderelationer og genererer indtægter medfører alt sammen omkostninger. Ofte kan omkostningerne beregnes let, når man først har defineret alle ressourcer, aktiviteter og partnere. (ibid.)  </p>
            
            
            <h3 class="h3" id="pers">Personlig refleksion</h3>
            
            <p class="tekst">Jeg kommer helt klart til at bruge BMC fremover. Jeg synes man får et god overblik over en virksomhed på meget kort tid. Denne metode kan man også bruger i Design Thinking under Emphathize fasen, hvis man f.eks. skulle udarbejde et produkt hvor man ønsker at have et overblik over virksomheden og deres forretningsmodel.   </p>
            
