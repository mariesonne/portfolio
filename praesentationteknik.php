            <h3 class="h3" id="mund-pras">Mundtlig præsentation</h3>    
        
            <p class="tekst-i-boks">Alle multimediedesignere kommer på et tidspunkt til at præsentere enten forslag eller resultater for andre. (Busch, Præsentation, 2015) Det kan f.eks. være internt for en chef eller eksternt for en kunde. Vi har allerede igennem 1. semester skulle præsentere flere forskellige projekter. Så allerede nu er præsentationsteknik meget relevant. De handler om at øve sig mange gange, så man kan udtrykke sig præcist og fremtræde naturligt når det virkelig gælder. (ibid.) Man kan træne sig selv til at blive en god taler.</p>
            
            <p class="tekst-i-boks">Ved den mundtlige præsentation skal man overveje hvad der er formålet med præsentationen. Spørg dig selv inden præsentationen hvorfor de skal hører på dig og hvad vil de gerne høre. (ibid.)</p>
            
        </section>
        
    
        
        <h3 class="h3" id="skabdia">Skab dialog</h3>
        
            
        <p id="citat">Få tilhørernes opmærksomhed, før du begynder.</p>
        
        <br><br>
        
        <p class="tekst">En god start betyder meget for hele præsentationen. Man skal skabe god kontakt til dem man præsenterer for, altså man skal altså skabe dialog helt fra starten. (ibid.) Denne kontakt er oftest ikke verbal. Men man kan tydeligt mærke uden ord om der er kontakt og om man dermed har deres opmærksomhed. Det kan være om de kigger ud ad vinduet eller ned i en telefon. Hvis dette er tilfældet, er der ikke skabt dialog. 
        </p>
        
        <p class="tekst">En måde at skabe dialog, kunne være at få deres opmærksomhed ved at sige ”jeg er klar når I er klar.”. Dette kan også siges uden ord, ved at stå afventende og stille. Når man har fået deres opmærksomhed, kan man begynde. (ibid.) Hvis nogle ikke ønsker at deltage i dialogen og rette fokus på præsentationen, skal man ignorere dem. Der er desværre ikke andet at gøre. Der kan ikke skabes dialog før der er kontakt, og dem man præsenterer for, skal også bidrage til dialogen.
        </p>
        
        <br><br>
        
        <h3 class="h3" id="kenddig">Kend dig selv</h3>
        
        <p id="citat">Vær bevidst om, hvad du mener.</p>
        
        <br><br>
        
        <p class="tekst">Det ikke nok, at have styr på de grundlæggende præsentationsteknikker. Det allervigtigste er, at man er oprigtig. Man skal være rolig og nærværende under præsentationen. (ibid.) Man skal hvile i sig selv, og det gør man bedst ved at have øvet sig. Det er vigtigt at man ved hvad man vil sige og hvorfor man vil sige det. Det man tænker man tænker og føler for et emne, skal man kunne udtrykke. Man skal derfor gøre det klart, hvad man egentligt mener. (ibid.)</p>
        
        <br><br>
        
        <h3 class="h3" id="malgruppe">Målgruppen</h3>
        
        <p id="citat">Hvad vil du tale om, hvordan vil du tale om det, og hvad vil du opnå – set i forhold til målgruppen. </p>
        
        <br><br>
        
        <p class="tekst">Hvem er målgruppen? Hvad vil de gerne høre? Hvilke spørgsmål har de? Hvorfor vil de hører præsentationen og hvad ønsker de at få ud af det? (ibid.) Der er stor forskel på en eksamen og en præsentation for en kunde. Uanset hvilken situation man er i, skal man huske at det er det man siger der i centrum. Man kan holde deres opmærksomhed ved at tage føringen i dialogen. Hvis man f.eks. er på forkant med de spørgsmål de kunne have, føler de sig tilgodeset og husket. Deres opmærksomhed og nysgerrighed vil derved stige. (ibid.) </p>
        
        <p class="tekst">Inden sin præsentation kan man gøre sig tanker om hvilke emner, man vil tage op og hvilket ordforråd man vil bruge i forhold til sin målgruppe. Hvis man præsenterer noget til f.eks. en eksamen, så er det vigtigt at man kan bruge faglige termer. Men hvis du f.eks. præsenterer et resultat for en kunde, bliver man nødt til at tale det sprog kunden forstår. (ibid.) </p>
        
        <br><br>
        
        <h3 class="h3" id="op-struktur">Oplægsstruktur</h3>
        
        <p id="citat">Få struktur på præsentationen.</p>
        
        <br><br>
        
        <p class="tekst">Der er flere forskellige modeller til at strukturer sin præsentation. De tager alle sammen udgangspunkt i samme struktur:</p>
        
            <br>

                <p id="citat">Sig, hvad du gerne vil sige.</p>
                
                <br>
        
                <p id="citat">Sig det.</p>
        
                <br>
        
                <p id="citat">Sig, det du lige har sagt.</p>

            <br>
        
        
        <p class="tekst">Altså så man først fortæller lidt op hvad præsentationen handler om og hvad man vil komme ind på. (bid.) Så holder man selve oplægget. Til sidst afslutter man med at repeterer kort hvad man lige har sagt.</p>
        
        <section class="boks">
        
            <h4 class="h4boks">Fisken</h4>    
        
            <p class="tekst-i-boks">Fiske-modellen er en klassisk og simpel måde at opdele sin præsentation. 
            </p>
            
            <img id="fisk" src="billeder/fisk.png">
            
            <p class="tekst-i-boks">Fiskens hoved er begyndelsen. Det er en indledning, hvor man skal vække interessen hos dem man præsentere for. (ibid.) Indledningen kan påvirke hele oplægget, derfor skal man ikke være usikker. Det skal være indøvet så man kan sætte scenen. Man skal være konkret. En god sætning at begynde med, kunne være ”Jeg vil gerne holde et oplæg om målgruppeanalyse, fordi…”Man skal sige hvad man vil sige og hvorfor man vil sige det, og samtidig vise hvem man er. (ibid.)</p>
            
            
            <p class="tekst-i-boks">Midten af fisken er i alt indholdet. Det er i denne fase man primært skal bruge sin taletid. (ibid.) Man skal komme hele vejen rundt og få sagt alt det man gerne vil sige. Man kan eventuelt præsentere konklusionerne af en analyse og afrunde med gode pointer. Man skal skabe en god stemning, så man bliver husket. </p>
            
            <p class="tekst-i-boks">Halen er afslutningen. Halen på en fisk udvider sig inden den slutter. På samme måde skal man kort genåbne sine pointer. Man skal afrunde det man startede i indledningen. Det kan man f.eks. ved at sige: ”Svaret på mit indledende spørgsmål, om hvorvidt en målgruppeanalyse kan betale sig er derfor...”.</p>
                
        
            
            <p class="tekst-i-boks">Fisken er god og simpel model, der giver en god struktur over oplægget.</p>
            
        </section>
        
        <h3 class="h3" id="retorik">Retorik</h3>
        
        <p class="tekst">Retorik er oprindeligt talekunst. Det er et meget stort og anerkendt fagområde, som man kan bruge til at forberede og fremfører en tale. Tale skal her forstås som oplæg eller præsentation. Der er fem faser eller trin. (ibid.)</p>
        
        <h4>Inventio</h4>
        
        <p class="tekst">Det er her man opfinder og forbereder det stof der skal præsenteres. Det er også her man finder de billeder, teorier og eksempler man har brug for. Man overvejer her hvad præsentationen skal handle om og hvad formålet er med dette. (ibid.) Her kan man kigge på 3 talekategorier:</p>
        
        
        <p class="tekst"><span class="teksttyk">Festtalen </span>der har formålet at hylde eller dadle, med fokus på nuet. </p>
        <p class="tekst"><span class="teksttyk">Den politiske tale </span>skruer fremad med formålet at bringe fremtidige perspektiver i spil.</p>
        <p class="tekst"><span class="teksttyk">Den juridiske tale </span>skruer bagud og vurdere om noget er rigtigt eller forkert. </p>
    
        <p class="tekst">Man skal altså vurdere hvad man vil opnå med præsentationen. (ibid.)</p>
        
        <h4>Disposito</h4>
        
        <p class="tekst">Det er her man organiserer sit oplæg. Man skal udvælge de argumenter og forhold der skal frembringe budskabet. Man skal få delene af præsentationen struktureret i en god og logisk rækkefølge. (ibid.) I klassisk retorik opdeler man oplægget i 4:</p>
        
        <p class="tekst"><span class="teksttyk">Exordium  </span>som er indledningen </p>
        <p class="tekst"><span class="teksttyk">Narratio  </span>er sagsfremstillingen, fortællingen – den gode historie</p>
        <p class="tekst"><span class="teksttyk">Argumentatio  </span>er hvor man skal bevise. Man kan trække på Toulmins argumentationsteori om påstand, belæg og hjemmel (ibid.) </p>
        
        <h4>Elocutio </h4>
    
        <p class="tekst">Man skal her vurdere hvilket sprog og hvilken stil man vil anvende. Hvilke appelformer vil man anvende og hvordan? Udover pahtos, ethos og logos kan man også overveje fire forhold:</p>
        
        <p class="tekst"><span class="teksttyk">Aptum </span>hensigtmæssigt, relevant, tilpasset modtagers forhåndsvisning</p>
        <p class="tekst"><span class="teksttyk">Puritas </span>sproglig korrekt</p>
        <p class="tekst"><span class="teksttyk">Perspicuitas </span>klart og forståeligt</p>
        <p class="tekst"><span class="teksttyk">Ornatus </span>levende, varieret, afstemt stil</p>
        
        <h4>Memoria </h4>
        
        <p class="tekst">Man skal øve sig. Man skal lærer talen udenad så man kan fokusere på hvordan man vil træde frem.</p>
        
        <h4>Actio</h4>
        
        <p class="tekst">Det er selve præsentationen. </p>
        
        
        <h3 class="h3" id="øv">Øv dig</h3>
        
        <p class="tekst">Når indholdet er helt på plads, skal man øve sig højlydt. (ibid.) Det vil gøre det lettere at huske hvad man skal sige. Det er også oftest først når man begynder at formulere sig højt, at man opdager eventuelle huller og fejl. </p>
        
        <h4>Timing</h4>
        
        <p class="tekst">Som regel har man ikke lang tid til sin præsentation. Derfor skal man øve sig i at bruge tiden rigtigt. Det er vigtigt at man øver stoffet igennem, for at kunne vurdere hvor lang tid oplægget tager. Det skyldes at man taler langsommere når man forklarer end når man læser i et par noter. (ibid.) Man skal passe på med at tale for hurtigt. Pauser er naturlige og er er gode for både afsenderen, som kan nå at tænke undervejs, men også for modtageren, som kan nå at følge med. </p>
        
        <h4>Stemme og krop</h4>
        
        <p class="tekst">Når man øver sig, skal man også have fokus på hvordan man snakker og hvordan man står. Her er nogle ting, man kan tænke over, når man øver sig til en præsentation. </p>
        
        <p class="tekst">Man skal have front mod tilhørerne, når man henvender sig - Man skal ikke tale med ryggen til.</p>
        
        <p class="tekst">Man skal trække vejret dybt og folde sin stemme ud – man skal tale højt og tydeligt</p>
        
        <p class="tekst">Man skal holde naturlige pauser, det kan evt. være lige før og efter en væsentlig pointe</p>
        
        
        <p class="tekst">Man skal lytte til hvad man selv siger</p>
        
        <p class="tekst">Man skal ikke gå hvileløst rundt, det er bedre at stå med begge fødder plantet i gulvet</p>
        
        <p class="tekst">Man skal lade vær med at fumle med ting, så ingen bidden negle eller legen med kuglepen</p>
        
        <p class="tekst">Notater skal skrives på stikordskort. Det skal være kraftigere papir, da det giver et dårligt indtryk at komme med et krøllet stykke papir. </p>
        
        <p class="tekst">Man skal undgå ”øh”</p>
        
        <p class="tekst">Tre dybe indånderinger inden man går på. (ibid.)</p>
        
        <h3 class="h3" id="pp">Powerpoint</h3>
        
        <p class="tekst">PowerPoint er et rigtig godt værktøj, hvis man bruger det rigtigt. Man skal passe på med at bruge det som en huskeliste. (ibid.) Hvis der er mange ord, begynder tilskuerne at læse foran og nogle gange stopper helt med at lytte. I stedet for at lave huskelister, kan man bruge PowerPoint-slides til at illustrerer, at det man siger er rigtigt. Det kan man gøre med eksempler eller billeder. </p>
        
        
        <h3 class="h3" id="pers">Personlig reflektion</h3>
        
        <p class="tekst">At præsentere, er noget man aldrig kan stoppe med at øve sig på. Jeg lærer det bedst, ved at gøre det. For at blive bedre til at præsentere, så tænker jeg teknikkerne ind over når jeg f.eks. har en fremlæggelse i skolen eller lignende. Det er denne måde jeg lærer præsentationsteknikkerne bedst. </p>
        
