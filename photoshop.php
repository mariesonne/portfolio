            <h4>Curves</h4>
            
            <p class="tekst">Curves fungere lidt ligsom Levels. Men curves er meget mere kraftfuld og præcis. Vi har mulighed for at tilføje så mange punkter vi vil i histogrammet og vi kan derved justere alle toner i billedet. </p>
            
