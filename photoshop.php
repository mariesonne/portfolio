            <h3 class="h3" id="adjustment">Adjustments layers</h3>
            
            <p class="tekst">Som sagt kan man gøre brug af adjustments layers eller på dansk justeringslag når man redigerer sine billeder non-destructive. Vi har til hver en tid mulighed for at redigere eller fjerne justeringerne.</p>
            
            <h4>Brightness/contrast</h4>
            
            <p class="tekst">Med dette justeringslag kan vi justere lysstyrken på billedet. Derudover kan vi også tilføje eller fjerne kontrast til billedet.</p>
            
