            <h4>Levels</h4>
            
            <p class="tekst">Med Levels får vi histogrammet med shadows, mellemtoner og highlights hvor vi kan justere de forskellige niveauer. Dette er en god måde at være sikker på, at man har et perfekt histogram – og dermed et perfekt eksponeret billede.</p>
            
