            <h3 class="h3" id="ophav">Ophavsret </h3>
            
            <p class="tekst">Ophavsret fungerer som en form for skjold. (ibid.) Det beskytter både fysiske og digitale medier mod tyveri eller kopiring. Man må ikke stjæle varer i en fysisk butik, det er ulovligt. På samme måde er det altså også ulovligt at stjæle eller kopiere andres værker. (ibid.) Det kan være værker som for eksempel en skulptur, et billede, et lydklip osv. </p>
            
            <h4>Copyright</h4>
            
            <p class="tekst">Copyright er det amerikanske ord for ophavsret. (ibid.) Der er altså ikke forskel på at have copyright eller ophavsret på et værk. Copyright har et symbol, som man bruger når man har copyright eller ophavsret på noget. (ibid.) Ophavsret har ikke et symbol, så derfor bruger man altid copyright-symbolet. Det betyder ikke, at man skal have symbolet synligt, for at være beskyttet af ophavsretsloven. Så længe at ens værk lever op til kravene, så er man beskyttet. Hvis nogle kopierer ens værk, såstår man ikke dårligere i en retssag, bare fordi man ikke har anvendt symbolet. (ibid.) Men det kan være en god ide at bruge symbolet, for eksempel for at forebygge at nogle kopierer ens værk. </p>
            
            <p class="tekst">For multimediedesignere er copyright meget væsentligt, da man oftest skaber mange digitale værker. Herunder er der både websider, som man har ophavsret over. Websiden indeholder tekster, måske billeder eller videoer, som multimediedesigneren også har ophavsretten over. Derudover skal mutimediedesigneren modsat være opmærksom på, at der ikke bliver brugt tekster, billeder eller andre værker, som andre har ophavsretten over. Så det er altså ikke muligt at bruge et hvilket som helst billede, man lige finder på internettet for eksempel. </p>
            
            <p class="tekst">Når man laver en ny hjemmeside, er det vigtigt at den ikke ligner andre hjemmesider. Hvis to hjemmesider ligner hinanden for meget, vil søgemaskinerne markere det som ”duplicate content”. (ibid.) Hvis der er flere ensartede hjemmesider, så kan søgemaskinerne ikke altid vurdere hvordan de skal rangeres. Nogle gange vælger de at gøre den ene af siderne helt usynlig. (Ibid.) Da søgemaskinerne ikke altid kan vurdere hvilken side, der er den originale, er det meget vigtigt for multimediedesigneren, at sikre sig, at ingen kopiere indholdet. Derimod vil det også være en ulempe for multimediedesigneren, at kopiere fra andres sider. (Ibid.)</p>
            
            <h4>Copyright på de sociale medier</h4>
            
            <p class="tekst">Oftest når man opretter en profil på et social medie, det kan eksempelvis være Instagram, så giver man siden licens til at må bruge de værker man uploader. (Kultur ministeriet, u.d.) Men som udgangspunkt må andre ikke bruge de værker man uploader på sin profil. (ibid.) Man skal dog være opmærksom på, at de fleste medier har forskellige funktioner. Det kan blandt andet være mulighed for at dele, like osv. </p>
            
            <p class="tekst">Youtube har en funktion, hvor man kan embedde en video, på ens hjemmeside. (ibid.) Det er lovligt, når man bruger funktionen inde på siden. Det har de med rettighederne accepteret, da de oprettede profilen. Man skal dog være opmærksom på, at de danske domstole ikke har taget stilling til, om det gælder alle tilfælde. (ibid.) Det er vigtigt at personen, som har uploadede videoen også har ophavsretten. Hvis ikke personen har det, er det en overtrædelse af ophavsretsloven, og derfor er det ulovligt at embedde den, da det medvirker til en fornyet ophavsretskrænkelse. (Ibid.) </p>
        
         
