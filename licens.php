                <h3 class="h3" id="irettigheder">Immaterielle rettigheder  </h3> 
                
                <h4 class="h4boks">Hvad er immaterielle rettigheder?</h4>
                
                <p class="tekst-i-boks">Immaterielle rettigheder kan også kaldes IPR, som står for ”intellectual property rights”. (Bilag 4.3) Det betyder at man har en form for ejendomsret over sin ide eller konstruktion. (ibid.) Så hvis man opfinder en ny højtaler og producere 100 af dem, så dækker de immaterielle rettigheder ikke kun de 100 der er produceret, men også de der i fremtiden bliver produceret med udgangspunkt i samme ide. Immaterielle rettigheder er altså fællesbetegnelse for retsbeskyttelsen af intellektuelle frembringelser, fx kunst, billeder, litteratur, opfindelser, formgivning designs og forretningskendetegn. (ibid.) Der findes forskellige love. Her er det arten og anvendeligheden af ideen, der afgør hvilken lov man hører ind under. (ibid.) Der er ophavsretsloven, patentloven, brugsmodelloven, designloven og varemærkeloven. (ibid.)</p>
                
                <p class="tekst-i-boks">Vi kan bruge rettighederne til at sikre os, at der ikke er nogle der kopirer vores design, produkt eller andet værk. Det kan være svært at skille sig ud, hvis der findes andre virksomheder med præcis samme design, ide, slogan og måske endda logo. Derfor er det vigtigt at man kan bevare originaliteten. </p>
                
                 <h4 class="h4boks">Ophavsret</h4>
                
                <p class="tekst-i-boks">Denne beskyttelse omfatter litterære og kunstneriske værker uanset fremtrædelsesformen. (ibid.) Det kan altså eksempelvis være musik, fotografier eller computerprogrammer. Det fungerer altså som en skjold mod kopiring. (ibid.) Hvis man skal have ophavsret over et værk, så skal der være originalitet i vist omfang. </p>
                
                <h4 class="h4boks">Patent</h4>
                
                <p class="tekst-i-boks">Hvis man har patent på et værk, så kan man forhindre andre i at fremstille eller sælge ens værk uden tilladelse. (ibid.) Man skal have opfundet noget nyt, for at kunne søge om patent. Så det skal altså være meget originalt. Det må ikke minde for meget om noget, der allerede er patent på, eller noget der er nævnt i artikler osv. Det er en god ide, at søge om patent hvis man har opfundet noget nyt. Man skal dog være opmærksom på, at opfindelsen bliver offentliggjort, når man får patent på den. Hvis man får dansk patent, så gælder det kun i Danmark. (ibid.) Så hvis man ønsker patent i andre lande, så skal man søge det der. </p>
                
                <h4 class="h4boks">Brugsretsmodel</h4>
                
                <p class="tekst-i-boks">Brugsmodelret kaldes også ”det lille patent”. (ibid.) Ligesom ved pantent stilles der krav til, at værket skal være nyt og adskille sig merkant fra andre værker. Men i forhold til patent er brugsmodelret billigere og hurtigere at opnå. (ibid.) Der stilles heller ikke ligeså høje krav til produktets kvalitet. Derfor kan man søge brugsmodelret, for eksempel hvis man ikke kan opnå patent. Så vil ens ide eller værk stadig være beskyttet. </p>
                
                <h4 class="h4boks">Designbeskyttelse</h4>
                
                <p class="tekst-i-boks">Ved designbeskyttelse sikre man sig beskyttelse af sit produkts udseende. Det vil altså sige at man ikke må kopirer designet eller mønstret. (ibid.) For at få beskyttelse af designet eller mønstret, skal det som ved de andre love være helt nyt. Man må ikke kunne finde det andre steder. (ibid.) Man kan søge om designbeskyttelse for hele produktet eller bare en del af produktet. Det kan være smart, hvis man har opfundet et produkt, hvor det kun er en del af produktet, der er nyt og unikt. Modsat af patentloven, så dækker designbeskyttelse i hele EU. (ibid.)</p>
                
                <h4 class="h4boks">Varemærkeregistrering</h4>
                
                
                <p class="tekst-i-boks">Når man registrer sit varemærke, så forhindre man andre i at kopiere eller misbruge det. (ibid.) Man bruger ofte sit varemærke til at definere sin virksomhed, sit produkt eller sig selv. Derfor kan det være en god ide at registrere det. Det kan eksempelvis være et logo, slogan, en vares form eller noget andet, der kan gengives grafisk. (ibid.) Man skal være opmærksom på, at den ikke dækker virksomhedens navn. Hvis man skal have beskyttet sit virksomhedsnavn, er der andre love og regler til det. (ibid.) </p>
            

